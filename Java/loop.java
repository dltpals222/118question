public class loop {
  public static void zeroNine(){
    for(int i = 0 ; i < 10 ; i++){
      System.out.print(i);
    }
  }

  public static void oneTen(){
    for(int i = 1 ; i <= 10 ; i++){
      System.out.print(i);
    }
  }
  public static void main (String[] args) {
    zeroNine();
    oneTen();
  }
}